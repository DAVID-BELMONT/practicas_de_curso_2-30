


numero: int = int(input("introduce un numero..\n"))   # \n esto hace que el codigo tenga un salto delinea 

print(numero * 5) # multiplicacion *
print(numero / 5) # divicion/
print(numero // 5) # divicion resultado enytero //
print(numero % 5) # divicion resutado residuo %
print(numero + 5) # suma +2
print(numero - 5) # multiplicacion
print(numero ** 5) # multiplicacion


